/**
 * Позиция корзины в виде, в каком её отдаёт и принимает бэкенд.
 */
export interface CartItem {
    /** Идентификатор блюда. */
    dish_id: number;
    /** Название блюда. */
    name: string;
    /** Цена за единицу в микрорублях. */
    price: number;
    /** Количество единиц в корзине. */
    quantity: number;
    /** Ссылка на изображение блюда. */
    image_url: string;
    /** Идентификатор пользователя-владельца позиции в групповом режиме. */
    owner_user_id?: number | null;
}

/**
 * Минимальный набор полей блюда, требуемых для добавления его в корзину.
 */
export interface DishToAdd {
    /** Идентификатор блюда. */
    id: number;
    /** Название блюда. */
    name: string;
    /** Цена за единицу в микрорублях. */
    price: number;
    /** Ссылка на изображение блюда. */
    image_url: string;
}

/**
 * Участник групповой корзины.
 */
export interface CartMember {
    /** Идентификатор пользователя. */
    user_id: number;
    /** ISO-метка времени присоединения к корзине. */
    joined_at: string;
}

/**
 * Снимок состояния корзины, возвращаемый бэкендом и используемый сторами для
 * полной перезаписи локального состояния.
 */
export interface CartSnapshot {
    /** Идентификатор корзины или `null`, если корзина ещё не создана. */
    cartId: string | null;
    /** Позиции корзины. */
    items: CartItem[];
    /** Идентификатор ресторана, к которому привязана корзина (`0`, если корзина пуста). */
    restaurantId: number;
    /** Режим корзины: `solo` или групповой. */
    mode: string;
    /** Статус групповой комнаты. */
    roomStatus: string;
    /** Идентификатор администратора групповой корзины. */
    adminId: number | null;
    /** Список участников групповой корзины. */
    members: CartMember[];
    /** Итоговая стоимость корзины в микрорублях. */
    totalCost: number;
}

/**
 * Состояние асинхронной загрузки и синхронизации корзины.
 *
 * - `idle` - запросов не выполняется;
 * - `loading` - идёт первичная загрузка;
 * - `syncing` - идёт фоновая синхронизация после изменения;
 * - `error` - последний запрос завершился ошибкой.
 */
export type CartStatus = 'idle' | 'loading' | 'syncing' | 'error';

/**
 * Полное состояние стора корзины: снимок плюс мета-поля синхронизации.
 */
export interface CartState extends CartSnapshot {
    /** Состояние асинхронных операций. */
    status: CartStatus;
    /** Текст последней ошибки, если она была. */
    error?: string;
}

/**
 * Колбэк, через который стор корзины спрашивает у вызывающего кода
 * подтверждение деструктивного действия (например, очистки корзины при
 * добавлении блюда из другого ресторана). Должен резолвиться в `true`, если
 * пользователь подтвердил, и в `false` иначе.
 */
export type CartConfirmer = () => Promise<boolean>;

/**
 * Приглашение в совместную корзину: токен и срок его жизни.
 */
export interface CartInvite {
    /** Токен приглашения, по которому другой пользователь присоединяется к корзине. */
    token: string;
    /** ISO-метка времени, после которой токен недействителен. */
    expiresAt: string;
}

/**
 * Событие WebSocket-канала совместной корзины.
 *
 * Бэкенд шлёт его при любом изменении общей корзины (добавление и удаление
 * позиций, состав участников, закрытие комнаты). Поле `payload` оставлено
 * нетипизированным: фронтенд на любое событие просто перечитывает корзину
 * целиком, а не применяет дельту.
 */
export interface CartWsEvent {
    /** Идентификатор корзины, к которой относится событие. */
    cart_id?: string;
    /** Тип события (`CartItemAdded`, `MemberJoined`, `SharedCartClosed` и т. п.). */
    event_type?: string;
    /** Произвольная полезная нагрузка события. */
    payload?: unknown;
}
