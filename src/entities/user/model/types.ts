/**
 * Профиль пользователя в форме, которую отдаёт бэкенд.
 */
export interface User {
    /** Числовой идентификатор пользователя (нужен для совместной корзины). */
    id: number;
    /** Отображаемое имя. */
    name: string;
    /** Email-адрес. */
    email: string;
    /** Ссылка на аватар. */
    avatar_url: string;
    /** Длина текущей серии заказов в неделях (геймификация). */
    streak_weeks?: number;
}

/**
 * Состояние асинхронной загрузки профиля.
 *
 * - `idle` - запросов не выполняется;
 * - `loading` - идёт загрузка;
 * - `error` - последний запрос завершился ошибкой.
 */
export type UserStatus = 'idle' | 'loading' | 'error';

/**
 * Состояние стора пользователя.
 */
export interface UserState {
    /** Текущий пользователь или `null`, если он не авторизован. */
    user: User | null;
    /** Состояние асинхронных операций. */
    status: UserStatus;
}
