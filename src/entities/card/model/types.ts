/**
 * Привязанная банковская карта пользователя.
 */
export interface Card {
    /** Идентификатор карты на стороне платёжного провайдера. */
    id: string;
    /** Последние четыре цифры PAN. */
    last4: string;
    /** Название банка-эмитента (если известно). */
    issuer_name?: string;
    /** Тип карты (например, `Visa`, `MasterCard`). */
    card_type?: string;
    /** Признак карты по умолчанию для оплаты. */
    is_default: boolean;
}

/**
 * Ответ эндпоинта привязки карты.
 */
export interface CardBindResponse {
    /** URL страницы подтверждения, куда нужно отправить пользователя. */
    confirmation_url?: string;
}

/**
 * Состояние асинхронной загрузки карт.
 *
 * - `idle` - запросов не выполняется;
 * - `loading` - идёт первичная загрузка;
 * - `syncing` - идёт фоновая синхронизация после изменения;
 * - `error` - последний запрос завершился ошибкой.
 */
export type CardStatus = 'idle' | 'loading' | 'syncing' | 'error';

/**
 * Состояние стора карт.
 */
export interface CardState {
    /** Список привязанных карт. */
    cards: Card[];
    /** Состояние асинхронных операций. */
    status: CardStatus;
}
