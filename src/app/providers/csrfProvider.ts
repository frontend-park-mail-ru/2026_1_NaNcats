import { httpClient } from '@shared/api/http';

/**
 * Запрашивает у бэкенда первичный CSRF-токен и сохраняет его в HTTP-клиенте.
 *
 * Вызывается на старте приложения, чтобы последующие изменяющие запросы могли
 * подставить актуальный токен. Сетевая ошибка не пробрасывается: приложение
 * должно стартовать даже при недоступном эндпоинте, проблема логируется в
 * консоль и проявится позже на конкретном защищённом запросе.
 *
 * @returns Промис, разрешающийся после попытки получить токен.
 */
export const initCsrf = async (): Promise<void> => {
    try {
        await httpClient.fetchCsrf();
    } catch (e) {
        console.warn('csrfProvider: fetchCsrf failed', e);
    }
};
