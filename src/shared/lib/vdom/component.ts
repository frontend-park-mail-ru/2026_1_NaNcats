/**
 * Хуки жизненного цикла функционального компонента.
 *
 * Здесь живут две точки расширения: onMount планирует колбэк после того, как
 * текущий синхронный mount-кадр завершится, а onCleanup реэкспортируется из
 * пакета сигналов и привязывает колбэк к жизненному циклу текущего owner.
 *
 * Деталь реализации onMount: на этом этапе достаточно queueMicrotask. Mount в
 * нашем ядре синхронный, поэтому к моменту, когда микротаск исполнится, DOM
 * уже вставлен в родительский контейнер. Полная интеграция с фазами mount
 * (после layout, до paint) появится позже, если этого потребует Unit 9.
 */

import { getOwner, onCleanup as ownerOnCleanup, runWithOwner } from '@shared/lib/signals';

/**
 * Планирует колбэк, который будет вызван после завершения текущего mount-кадра.
 *
 * Колбэк ставится в очередь микротасков, поэтому он исполнится сразу после
 * того, как стек синхронных вызовов render/mount размотается. Это даёт
 * пользователю гарантию: внутри колбэка DOM-дерево, описанное JSX, уже вставлено
 * в документ и доступно для измерений или подписок на внешние источники.
 *
 * Колбэк запускается под тем же owner, под которым был зарегистрирован onMount.
 * Это значит, что любые onCleanup, signal или effect, созданные внутри
 * колбэка, корректно привязываются к жизненному циклу компонента и снимутся
 * при размонтировании.
 *
 * Ошибки внутри колбэка не валят основной поток: они логируются в console.error.
 * Так одна неудачная подписка не остановит остальные mount-эффекты в дереве.
 *
 * @param cb Колбэк, который надо вызвать после mount.
 */
export function onMount(cb: () => void): void {
    const owner = getOwner();
    queueMicrotask(() => {
        if (owner && owner.disposed) {
            return;
        }
        try {
            if (owner) {
                runWithOwner(owner, cb);
            } else {
                cb();
            }
        } catch (err) {
            console.error('[vdom] onMount throw:', err);
        }
    });
}

export { ownerOnCleanup as onCleanup };
