/**
 * Описание ядра типов VDOM: форма VNode, виды детей, ссылки на DOM и
 * минимальный контракт функционального компонента.
 *
 * Файл содержит только декларации типов (никакой реализации), чтобы остальные
 * модули ядра (h, render, patch, props, diff-children, portal) могли свободно
 * импортировать форму VNode без циклических зависимостей.
 */

/**
 * Колбэк-ссылка: получает смонтированный DOM-узел или null при размонтировании.
 *
 * @template T Тип целевого DOM-узла, который удерживает ссылка.
 */
export type RefCallback<T> = (el: T | null) => void;

/**
 * Объектная ссылка с полем current: совместима с привычной формой ref.current,
 * заполняется при монтировании и зануляется при размонтировании.
 *
 * @template T Тип целевого DOM-узла, который удерживает ссылка.
 */
export interface RefObject<T> {
    current: T | null;
}

/**
 * Объединённая форма ссылки на DOM-узел: либо колбэк, либо объект с current.
 *
 * @template T Тип целевого DOM-узла, который удерживает ссылка.
 */
export type Ref<T> = RefCallback<T> | RefObject<T>;

/**
 * Допустимое значение ключа дочернего VNode для согласования списков.
 *
 * Ключ нужен для алгоритма keyed-diff в diff-children: одинаковый ключ при
 * одинаковом type удерживает узел на месте, иначе узел пересоздаётся.
 */
export type Key = string | number;

/**
 * Возможные сырые формы, которые принимает фабрика h в качестве ребёнка.
 *
 * - VNode: вложенный узел.
 * - string / number: текстовый литерал (станет Text-узлом при монтировании).
 * - null / undefined / false / true: игнорируется при нормализации, что
 *   удобно для условных вставок вида `{cond && <X/>}`.
 * - VNodeChild[]: вложенный массив; нормализатор разворачивает один уровень.
 */
export type VNodeChild =
    | VNode
    | string
    | number
    | boolean
    | null
    | undefined
    | VNodeChild[];

/**
 * Удобный псевдоним для пропа children, который принимает фабрика h.
 *
 * Совпадает по форме с VNodeChild, но именован отдельно для читаемости в
 * сигнатурах компонентов и в JSX-runtime.
 */
export type ComponentChildren = VNodeChild;

/**
 * Нормализованный ребёнок после прохода через h: либо VNode, либо примитив.
 *
 * Примитивы (string/number) намеренно не оборачиваются в текстовые VNode,
 * чтобы массив детей оставался плоским и дешёвым в обходе. Текстовые DOM-узлы
 * создаются на стадии render/patch.
 */
export type NormalizedChild = VNode | string | number;

/**
 * Сырые пропсы, которые приходят на вход h: произвольная карта, плюс
 * специальные поля key, ref, children.
 *
 * После прохода через h поля key и ref выносятся на уровень VNode, а children
 * нормализуются в массив; в итоговый VNode.props эти поля не попадают.
 */
export interface RawProps {
    key?: Key;
    ref?: Ref<Element>;
    children?: ComponentChildren;
    [prop: string]: unknown;
}

/**
 * Финальная карта пропсов внутри VNode: без key, без ref, без children.
 *
 * Это намеренно открытая карта с типом значения unknown: конкретные модули
 * (props.ts) сами разбирают известные ключи (class, style, on*-обработчики)
 * и пишут остальные как атрибуты.
 */
export type VNodeProps = Readonly<Record<string, unknown>>;

/**
 * Дескриптор виртуального узла: единица описания дерева, с которой работают
 * render, patch и diff-children.
 *
 * Поля __dom и __instance являются внутренними: они выставляются ядром во
 * время монтирования и патча, чтобы повторные патчи могли работать в-месте.
 * Пользовательский код их не заполняет и читать их не обязан.
 */
export interface VNode<P extends VNodeProps = VNodeProps> {
    /**
     * Тип узла: имя HTML-тега, функция-компонент или маркер-символ
     * (Fragment, портал).
     */
    type: string | symbol | Component<P>;
    /** Пропсы без служебных полей key, ref и children. */
    props: P;
    /** Нормализованный массив детей: VNode либо примитив (string/number). */
    children: NormalizedChild[];
    /** Ключ для согласования списков; отсутствует для не-ключевых узлов. */
    key?: Key;
    /** Ссылка на DOM, заполняется при монтировании. */
    ref?: Ref<Element>;
    /**
     * Связанный DOM-узел. Заполняется ядром при монтировании, читается при
     * патче для согласования в-месте.
     *
     * - Для узлов с тегом-строкой это конкретный Element.
     * - Для текстовых детей-примитивов отдельного VNode нет, текстовый узел
     *   принадлежит родителю.
     * - Для Fragment это массив верхнеуровневых DOM-узлов фрагмента (может
     *   быть пустым).
     * - Для функциональных компонентов это DOM-узлы их внутреннего поддерева.
     * - Для портала это пустой массив (в исходном контейнере портал не
     *   оставляет видимых узлов), а реальные дети живут внутри target из
     *   props портала.
     */
    __dom?: Node | Node[] | null;
    /**
     * Внутреннее поддерево, в которое раскрылся функциональный компонент или
     * портал. Используется для последующего патча: при стабильном type+key
     * ядро патчит __instance.next против предыдущего __instance, не пересоздавая
     * сам внешний VNode.
     */
    __instance?: VNode | null;
}

/**
 * Функциональный компонент: чистая функция, принимающая пропсы и возвращающая
 * VNode (или примитив, либо null/false для пустого рендера).
 *
 * Параметр P намеренно ограничен object, а не VNodeProps: внутри ядра VNode.props
 * хранится как открытая карта (VNodeProps), но в сигнатуре компонента
 * пользователю удобнее описывать пропсы обычным интерфейсом с конкретными
 * полями. Структурная совместимость обеспечивается тем, что ядро при патче и
 * при вызове компонента не предполагает никаких полей кроме явно прочитанных.
 *
 * Реактивный жизненный цикл (onMount/onCleanup, контексты) живёт в модуле
 * vdom/component.ts и vdom/context.tsx: здесь определена только базовая форма,
 * чтобы ядро могло вызывать компонент и патчить его выход.
 *
 * @template P Тип пропсов компонента.
 */
export type Component<P extends object = VNodeProps> = (props: P) => VNode;
