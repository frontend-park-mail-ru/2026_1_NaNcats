/**
 * Карта известных URL-путей фронтенда.
 *
 * Используется как единственный источник правды для роутера и навигации:
 * все компоненты ссылаются на ROUTES.* вместо строковых литералов, поэтому
 * переименование пути остаётся в одном месте. Объект помечен as const,
 * чтобы значения становились строковыми литералами и попадали в {@link RoutePath}.
 */
export const ROUTES = {
    /** Главная страница со списком ресторанов. */
    home: '/',
    /** Страница входа в аккаунт. */
    login: '/login',
    /** Страница регистрации нового пользователя. */
    register: '/register',
    /** Страница ресторана со списком блюд. */
    restaurant: '/restaurant',
    /** Личный кабинет пользователя. */
    profile: '/profile',
    /** Страница оформления заказа. */
    checkout: '/checkout',
    /** Страница для несуществующих маршрутов. */
    notFound: '/404',
} as const;

/** Объединённый литеральный тип всех путей из {@link ROUTES}. */
export type RoutePath = (typeof ROUTES)[keyof typeof ROUTES];
